s1 = {10,20,30,40}
s2 = {40,50,60,70}

#a.Set difference
print("Set differnce")
print("\nInitially set 's1' is : ",s1)
print("Initially set 's2' is : ",s2)
s3 = s1.difference(s2)
print("Updated set 's1' is : ",s1)
print("Updated set 's2' is : ",s2)
print("Set difference is : ",s3)

#b.Symmetric difference
print("\nSymmetric difference")
print("Initially set 's1' is : ",s1)
print("Initially set 's2' is : ",s2)
s4 = s1.symmetric_difference(s2)
print("Updated set 's1' is : ",s1)
print("Updated set 's2' is : ",s2)
print("Symmetric difference is : ",s4)




